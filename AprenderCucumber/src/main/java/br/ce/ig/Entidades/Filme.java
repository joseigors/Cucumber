package br.ce.ig.Entidades;

public class Filme {
	private int estoque;
	private int aluguel;
	
	
	
	public void setEstoque(int arg1) {
		this.estoque = arg1;
	}
	public void setAluguel(int preço) {
		this.aluguel = preço;
		
	}
	public int getAluguel() {
		
		return aluguel;
	}
	public int getEstoque() {
		return estoque;
	}
	
	

}
